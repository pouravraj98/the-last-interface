/** Get a product by its slug ID */
export function getProductById(id) {
  return products.find((p) => p.id === id) || null
}

/** Get a product by numeric index (for AI tool calls) */
export function getProductByIndex(index) {
  return products[index] || null
}

/** Get products by category */
export function getProductsByCategory(category) {
  return products.filter((p) => p.category === category)
}

/** Get products by collection slug */
export function getProductsByCollection(collection) {
  return products.filter((p) => p.collections.includes(collection))
}

/** Get all unique categories */
export function getCategories() {
  return [...new Set(products.map((p) => p.category))]
}
